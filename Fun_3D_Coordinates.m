function Result = Fun_3D_Coordinates (b,c,p1,p2)
Result=zeros(17,3);
for i=1:17
    A= [b(1,1)-b(3,1)*p1(i,1),b(1,2)-b(3,2)*p1(i,1),b(1,3)-b(3,3)*p1(i,1);
        b(2,1)-b(3,1)*p1(i,2),b(2,2)-b(3,2)*p1(i,2),b(2,3)-b(3,3)*p1(i,2);
        c(1,1)-c(3,1)*p2(i,1),c(1,2)-c(3,2)*p2(i,1),c(1,3)-c(3,3)*p2(i,1);
        c(2,1)-c(3,1)*p2(i,2),c(2,2)-c(3,2)*p2(i,2),c(2,3)-c(3,3)*p2(i,2)];
    
    B=[p1(i,1)-b(1,4);p1(i,2)-b(2,4);p2(i,1)-c(1,4);p2(i,2)-c(2,4)];
    Result(i,:)=pinv(A)*B;
end
end
